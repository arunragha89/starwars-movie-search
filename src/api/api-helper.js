import { ROOT_DOMAIN } from "../constants/service-constants";

const apiHelper = (
  endpoint,
  { body, ...customConfig } = {},
  apiUrl = ROOT_DOMAIN
) => {
  const headers = { "content-type": "application/json" };

  const config = {
    method: body ? "POST" : "GET",
    ...customConfig,
    headers: {
      ...headers,
      ...customConfig.headers,
    },
  };

  if (body) {
    config.body = body;
  }

  return window
    .fetch(`${apiUrl}/${endpoint}`, config)
    .then(async (response) => {
      const data = await response.json();
      if (response.ok) {
        return data;
      } else {
        return Promise.reject(data);
      }
    });
};

export default apiHelper;
